class Myuser {
  String gender;
  String name;
  String location;
  String email;

  String dob;

  String phone;

  String id;
  String picture;

  Myuser(
      {this.id,
      this.name,
      this.location,
      this.email,
      this.dob,
      this.phone,
      this.gender,
      this.picture});

  factory Myuser.fromDocument(DocumentSnapshot doc) {
    return Myuser(
        id: doc['id'],
        name: doc['Name'],
        location: doc['location'],
        email: doc['email'],
        dob: doc['dob'],
        phone: doc['phone'],
        gender: doc['gender'],
        picture: doc["picture"]);
  }
}
